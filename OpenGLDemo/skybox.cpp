Skybox::Skybox(const std::string &right, const std::string &left, const std::string &top,
	const std::string &bottom, const std::string &front, const std::string &back) {
	std::vector<std::string> faces;
	faces.reserve(6);
	faces.push_back(right);
	faces.push_back(left);
	faces.push_back(top);
	faces.push_back(bottom);
	faces.push_back(front);
	faces.push_back(back);
	Setup(faces);
}

Skybox::Skybox(const std::vector<std::string> &faces) {
	if (faces.size() != 6) {
		printf("Skybox constructor needs 6 faces in the correct order!");
	} else {
		Setup(faces);
	}
}

void Skybox::Draw(const Shader &skyboxShader, Camera & camera) {
	glDepthMask(GL_FALSE);
	glUseProgram(skyboxShader.programID);

	glm::mat4 view = glm::mat4(glm::mat3(camera.viewMatrix()));

	glUniformMatrix4fv(skyboxShader.viewMatID, 1, GL_FALSE, &view[0][0]);
	glUniformMatrix4fv(skyboxShader.projMatID, 1, GL_FALSE, &camera.projMatrix(camera.aspectRatio)[0][0]);


	glBindVertexArray(VAO);
	glActiveTexture(GL_TEXTURE2);
	glBindTexture(GL_TEXTURE_CUBE_MAP, textureID);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
	glDepthMask(GL_TRUE);

}

void Skybox::Setup(const std::vector<std::string> &faces) {
	//cube to draw to
	glGenVertexArrays(1, &VAO);
	glGenBuffers(1, &VBO);
	glBindVertexArray(VAO);
	glBindBuffer(GL_ARRAY_BUFFER, VBO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(cubeVertices), &cubeVertices, GL_STATIC_DRAW);
	glEnableVertexAttribArray(0);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);

	glBindVertexArray(0);

	//actual texture
	glGenTextures(1, &textureID);
	glBindTexture(GL_TEXTURE_CUBE_MAP, textureID);

	stbi_set_flip_vertically_on_load(false);

	for (unsigned int i = 0; i < 6; i++) {
		unsigned char *data = stbi_load(faces[i].c_str(), &width, &height, &colorChannels, STBI_rgb);
		if (data) {
			glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, GL_RGB, width, height, 0, GL_RGB, GL_UNSIGNED_BYTE, data);
		} else {
			printf("Failed to load skybox: %s", faces[i]);
		}

		stbi_image_free(data);
	}
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
}
